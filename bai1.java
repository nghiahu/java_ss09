package baitap;
    /*
    * -- Class
    * - Định nghĩa:  Class là một bản thiết kế (blueprint) để tạo ra các đối tượng.
    * Nó định nghĩa các thuộc tính (attributes) và hành vi (methods) của một nhóm đối tượng.
    * - Vai trò: Đóng vai trò như một khuôn mẫu để tạo ra các đối tượng. Nó xác định cấu trúc và hành vi của các đối tượng.
    *
    * Ví dụ thực tế:
    *public class Student {
    *    String studentName;
    *    int age;
    *   public void hello(){
    *        System.out.println("Xin chào bạn");
    *   }
    * }
    /*
    * Từ khóa liên quan:
    * - class,static,extends,implements,abstract,final,synchronized,native,enum
    * */

    /*
    * -- Object
    * - Định nghĩa: Là một thể hiện (instance) cụ thể của một class.
    *  Object chứa dữ liệu và có thể thực hiện các hành động thông qua các phương thức được định nghĩa trong class.
    * - Vai trò: Đóng vai trò là thực thể cụ thể trong chương trình.
    *   Mỗi object có trạng thái riêng (giá trị của các thuộc tính) và có thể tương tác với các object khác.
    *
    *-  Ví dụ thực tế:
    * Student student = new Student();
    * student.studentName = "Nguyễn Văn B";
    * student.age = 19;
    * student.hello();
    * - từ khóa liên quan: new,this,super,instanceof,finalize,
     */


